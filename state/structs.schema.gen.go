type ValueRaw struct {
	t   uint8
	val string
}

func (d *ValueRaw) Size() (s uint64) {

	{
		l := uint64(len(d.val))

		{

			t := l
			for t >= 0x80 {
				t >>= 7
				s++
			}
			s++

		}
		s += l
	}
	s += 1
	return
}
func (d *ValueRaw) Marshal(buf []byte) ([]byte, error) {
	size := d.Size()
	{
		if uint64(cap(buf)) >= size {
			buf = buf[:size]
		} else {
			buf = make([]byte, size)
		}
	}
	i := uint64(0)

	{

		buf[0+0] = byte(d.t >> 0)

	}
	{
		l := uint64(len(d.val))

		{

			t := uint64(l)

			for t >= 0x80 {
				buf[i+1] = byte(t) | 0x80
				t >>= 7
				i++
			}
			buf[i+1] = byte(t)
			i++

		}
		copy(buf[i+1:], d.val)
		i += l
	}
	return buf[:i+1], nil
}

func (d *ValueRaw) Unmarshal(buf []byte) (uint64, error) {
	i := uint64(0)

	{

		d.t = 0 | (uint8(buf[i+0+0]) << 0)

	}
	{
		l := uint64(0)

		{

			bs := uint8(7)
			t := uint64(buf[i+1] & 0x7F)
			for buf[i+1]&0x80 == 0x80 {
				i++
				t |= uint64(buf[i+1]&0x7F) << bs
				bs += 7
			}
			i++

			l = t

		}
		d.val = string(buf[i+1 : i+1+l])
		i += l
	}
	return i + 1, nil
}

